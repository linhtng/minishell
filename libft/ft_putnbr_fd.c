#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0 && n >= -9)
	{
		ft_putchar_fd('-', fd);
	}
	if (n > 9 || n < -9)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	n = (n % 10);
	if (n < 0)
	{
		n *= -1;
	}
	n += 48;
	ft_putchar_fd(n, fd);
}
